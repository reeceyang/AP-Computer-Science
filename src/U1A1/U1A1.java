// This program will display a face on
// the console window.

public class U1A1{

	public static void main(String[] args){

		// Prints the hair
		System.out.print("  ///// \n");
		// Prints the forehead
		System.out.println(" |     |");
		// Prints the eyebrows
		System.out.println(" | \" \" |");
		// Prints the eyes
		System.out.println(" | o o |");
		// Prints the ears and the nose
		System.out.println("(|  ^  |)");
		// Prints the mouth
		System.out.println(" | \\_/ |");
		// Prints the chin
		System.out.println("  ----- ");
	}
}

